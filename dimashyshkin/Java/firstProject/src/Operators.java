public class Operators {
    public static void main(String[] args) {
         int a = 10;
         int b = 5;

        System.out.println(a + b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a - b);

        System.out.println(++a);
        System.out.println(++a);

        System.out.println(--b);
        System.out.println(--b);

        System.out.println(a);
        System.out.println(b);

        int sum = a + b;
        System.out.println(sum);

        System.out.println(a == b);
        System.out.println(a != b);

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);

        System.out.println(3 > 2 && 10 < 9);
        System.out.println(3 > 2 || 10 < 9);




    }
}
